def sqlite_url(path: Path) -> str:
    return f"sqlite:///{path}"


def test_db_facade_exports_expected_schema_symbols() -> None:
    db = importlib.import_module("pi_memory.db")

    missing_attrs = [name for name in EXPECTED_DB_EXPORTS if not hasattr(db, name)]
    missing_exports = [name for name in EXPECTED_DB_EXPORTS if name not in db.__all__]

    assert missing_attrs == []
    assert missing_exports == []


def test_schema_compatibility_module_exports_expected_symbols() -> None:
    schema = importlib.import_module("pi_memory.db.schema")

    missing = [name for name in EXPECTED_SCHEMA_EXPORTS if not hasattr(schema, name)]

    assert missing == []


def test_schema_facades_share_model_and_base_objects() -> None:
    db = importlib.import_module("pi_memory.db")
    schema = importlib.import_module("pi_memory.db.schema")
    model_names = [
        name
        for name in EXPECTED_SCHEMA_EXPORTS
        if name == "Base" or not name.isupper()
    ]

    mismatched = [
        name
        for name in model_names
        if getattr(db, name) is not getattr(schema, name)
    ]

    assert mismatched == []


def test_base_metadata_registers_expected_tables() -> None:
    assert set(Base.metadata.tables) == EXPECTED_TABLE_NAMES


def test_schema_mappers_configure() -> None:
    configure_mappers()


def test_database_initialize_creates_registered_tables(tmp_path: Path) -> None:
    database = Database(sqlite_url(tmp_path / "memory.db"))
    try:
        database.initialize()
        table_names = set(inspect(database.engine).get_table_names())
    finally:
        database.close_if_open()

    assert EXPECTED_TABLE_NAMES.issubset(table_names)
